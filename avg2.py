# avg2.py

def main():
    print("This program computes the average of three exam scores.")

    score1 = float(input("Enter three scores separated by enter: "))
    score2 = float(input())
    score3 = float(input())
    average = (score1 + score2 + score3) / 3.0

    print("The average of the scores is:", average)


main()
